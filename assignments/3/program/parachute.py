class Parachute:
    def __init__ (self, openingSpeed, targetSpeed, duration, dragCoefficient, frontalArea, openingAltitude : 0):
        self.openingSpeed = openingSpeed
        self.targetSpeed = targetSpeed
        self.duration = duration
        self.dragCoefficient = dragCoefficient
        self.frontalArea = frontalArea
        self.openingAltitude = openingAltitude